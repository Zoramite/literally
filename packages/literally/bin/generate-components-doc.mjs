#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';

const inputFile = path.resolve(process.cwd(), 'custom-elements.json');
const outputFile = path.resolve(process.cwd(), 'COMPONENTS.md');

if (!fs.existsSync(inputFile)) {
  console.error(`Could not find ${inputFile}. Please run 'cem analyze' first.`);
  process.exit(1);
}

const manifest = JSON.parse(fs.readFileSync(inputFile, 'utf8'));
let md = `# UI Components\n\n`;
md += `This file is auto-generated by \`npm run docs:components\`. Do not edit manually.\n\n`;

const components = [];

for (const mod of manifest.modules || []) {
  for (const decl of mod.declarations || []) {
    if (decl.customElement && decl.tagName) {
      components.push({ modulePath: mod.path, ...decl });
    }
  }
}

if (components.length === 0) {
  md += `No components found.\n`;
} else {
  // Group by directory
  const groups = {};
  for (const comp of components) {
    const dir = path.dirname(comp.modulePath);
    if (!groups[dir]) groups[dir] = [];
    groups[dir].push(comp);
  }

  for (const [dir, comps] of Object.entries(groups).sort()) {
    md += `## \`${dir}\`\n\n`;
    for (const comp of comps.sort((a, b) => a.tagName.localeCompare(b.tagName))) {
      md += `### \`<${comp.tagName}>\` (${comp.name})\n`;
      md += `**File**: [${path.basename(comp.modulePath)}](${comp.modulePath})\n\n`;
      if (comp.description) {
        md += `${comp.description}\n\n`;
      } else {
        md += `No description provided.\n\n`;
      }

      // Attributes / Properties
      const props = comp.attributes || comp.members?.filter(m => m.kind === 'field' && !m.static && m.privacy !== 'private' && m.privacy !== 'protected') || [];
      if (props.length > 0) {
        md += `#### Properties & Attributes\n`;
        md += `| Name | Type | Default | Description |\n`;
        md += `|---|---|---|---|\n`;
        // Use a set to avoid duplicating props and attributes
        const seen = new Set();
        for (const attr of comp.attributes || []) {
          seen.add(attr.name);
          const type = attr.type?.text || 'string';
          const defaultVal = attr.default || '-';
          const desc = attr.description?.replace(/\n/g, ' ') || '';
          md += `| \`${attr.name}\` (attr) | \`${type}\` | \`${defaultVal}\` | ${desc} |\n`;
        }
        for (const prop of comp.members || []) {
          if (prop.kind === 'field' && !prop.static && prop.privacy !== 'private' && prop.privacy !== 'protected') {
            if (seen.has(prop.name)) continue;
            const type = prop.type?.text || 'any';
            const defaultVal = prop.default || '-';
            const desc = prop.description?.replace(/\n/g, ' ') || '';
            md += `| \`${prop.name}\` (prop) | \`${type}\` | \`${defaultVal}\` | ${desc} |\n`;
          }
        }
        md += `\n`;
      }

      // Slots
      if (comp.slots && comp.slots.length > 0) {
        md += `#### Slots\n`;
        md += `| Name | Description |\n`;
        md += `|---|---|\n`;
        for (const slot of comp.slots) {
          const name = slot.name === '' ? '*(default)*' : `\`${slot.name}\``;
          const desc = slot.description?.replace(/\n/g, ' ') || '';
          md += `| ${name} | ${desc} |\n`;
        }
        md += `\n`;
      }

      // Events
      if (comp.events && comp.events.length > 0) {
        md += `#### Events\n`;
        md += `| Name | Type | Description |\n`;
        md += `|---|---|---|\n`;
        for (const event of comp.events) {
          const type = event.type?.text || 'Event';
          const desc = event.description?.replace(/\n/g, ' ') || '';
          md += `| \`${event.name}\` | \`${type}\` | ${desc} |\n`;
        }
        md += `\n`;
      }

      // CSS Properties
      if (comp.cssProperties && comp.cssProperties.length > 0) {
        md += `#### CSS Custom Properties\n`;
        md += `| Name | Description |\n`;
        md += `|---|---|\n`;
        for (const cssProp of comp.cssProperties) {
          const desc = cssProp.description?.replace(/\n/g, ' ') || '';
          md += `| \`${cssProp.name}\` | ${desc} |\n`;
        }
        md += `\n`;
      }
    }
  }
}

fs.writeFileSync(outputFile, md, 'utf8');
console.log(`Generated COMPONENTS.md with ${components.length} components.`);
